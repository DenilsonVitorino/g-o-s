    private void btSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvaActionPerformed
        if ((txtNome.getText().isEmpty()) || (txtCpf.getText().isEmpty()) || (txtEndereco.getText().isEmpty()) || (            txtFone.getText().isEmpty()) || (txtEmail.getText().isEmpty()))
        {
            JOptionPane.showMessageDialog(null,"Todos os campos são obrigatórios!");
        }
        else
        {
            int ok = JOptionPane.showConfirmDialog(null,"Confirma os dados?","Atenção",JOptionPane.YES_NO_OPTION);
            if (ok==JOptionPane.YES_OPTION)
            {
                cm.setNome(txtNome.getText().toUpperCase().trim());
                cm.setCpf(txtCpf.getText().toUpperCase().trim());
                cm.setEndereco(txtEndereco.getText().toUpperCase().trim());
                cm.setFone(txtFone.getText().toUpperCase().trim());
                cm.setEmail(txtEmail.getText().toLowerCase().trim());
                cm.setResponsavel(FrmPrincipal.lblUsuario.getText());
                if (ne==0)
                {
                    cc.addCliente(cm);
                    carregaUltimoCliente();
                    carregaClienteId();
                }
                else
                {
                    cm.setId(txtCod.getText());
                    cc.altCliente(cm);
                    carregaClienteId();
                }
                controlesVisuais(true, false);
            }
        }
    }//GEN-LAST:event_btSalvaActionPerformed

