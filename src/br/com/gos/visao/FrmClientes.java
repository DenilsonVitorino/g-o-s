    public void carregaClienteId()
    {
        if (! txtCod.getText().isEmpty()) 
        {
            cm.setId(txtCod.getText());
            cc.buscaClienteId(cm);
            txtNome.setText(cm.getNome());
            txtCpf.setText(cm.getCpf());
            txtEndereco.setText(cm.getEndereco());
            txtFone.setText(cm.getFone());
            txtEmail.setText(cm.getEmail());
            txtAlterado.setText(cm.getAlterado());
            txtResponsavel.setText(cm.getResponsavel());
        }
    }

