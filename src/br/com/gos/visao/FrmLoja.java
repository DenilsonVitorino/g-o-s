    public void carregaLojaId()
    {
        if (! txtCod.getText().isEmpty()) 
        {
            lm.setId(txtCod.getText());
            lc.buscaLojaId(lm);
            txtRazao.setText(lm.getNome());
            txtCnpj.setText(lm.getCnpj());
            txtFone.setText(lm.getFone());
            txtEmail.setText(lm.getEmail());
            txtCadastro.setText(lm.getCadastro());
            txtPgto.setText(lm.getPgto());
            txtMensagem.setText(lm.getMensagem());
            if (lm.getBloqueado().equals("0"))
            {
                ckBloquear.setSelected(false);
            }
            else
            {
                ckBloquear.setSelected(true);
            }
        }
    }

