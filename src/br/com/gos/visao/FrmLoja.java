    private void btSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvaActionPerformed
        if ((txtRazao.getText().isEmpty()) || (txtEmail.getText().isEmpty()) || (txtFone.getText().isEmpty()))
        {
            JOptionPane.showMessageDialog(null,"Todos os campos são obrigatórios!");
        }
        else
        {
            int ok = JOptionPane.showConfirmDialog(null,"Confirma os dados?","Atenção",JOptionPane.YES_NO_OPTION);
            if (ok==JOptionPane.YES_OPTION)
            {
                String dia,mes,ano,dataFormatada;
                dia=txtCadastro.getText().substring(0, 2);
                mes=txtCadastro.getText().substring(3, 5);
                ano=txtCadastro.getText().substring(6);
                dataFormatada=ano+"-"+mes+"-"+dia;
                lm.setNome(txtRazao.getText().toUpperCase().trim());
                lm.setCnpj(txtCnpj.getText().toUpperCase().trim());
                lm.setFone(txtFone.getText().toUpperCase().trim());
                lm.setEmail(txtEmail.getText().toLowerCase().trim());
                lm.setCadastro(dataFormatada);
                lm.setPgto(txtPgto.getText().trim());
                lm.setMensagem(txtMensagem.getText().toUpperCase().trim());
                if (! ckBloquear.isSelected())
                {
                    lm.setBloqueado("0");
                } 
                else
                {
                    lm.setBloqueado("1");
                }
                if (ne==0)
                {
                    lc.addLoja(lm);
                    carregaUltimaLoja();
                    carregaLojaId();
                }
                else
                {
                    lm.setId(txtCod.getText());
                    lc.altLoja(lm);
                    carregaLojaId();
                }
                controlesVisuais(true, false);
            }
        }
    }//GEN-LAST:event_btSalvaActionPerformed

