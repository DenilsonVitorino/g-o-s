    public void carregaOs()
    {
        if (! txtCod.getText().isEmpty()) 
        {
            om.setOs(txtCod.getText());
            oc.buscaOs(om);
            txtEmissao.setText(om.getEmissao());
            txtCodCliente.setText(om.getIdCliente());
            txtNomeCliente.setText(om.getNomeCliente());
            cbxTipo.setSelectedItem(om.getTipo());
            cbxSituacao.setSelectedItem(om.getSituacao());
            txtEquipamento.setText(om.getEquipamento());
            txtDefeito.setText(om.getDefeito());
            txtServico.setText(om.getServico());
            txtPrevisao.setText(om.getEntrega());
            txtValor.setText(om.getValor().replace(".", ","));
            txtAlterado.setText(om.getAlterado());
            txtResponsavel.setText(om.getResponsavel());
        }
    }
    
